namespace paygw_mtnafrica\event;

/**
 * This event is triggered whenever a https request occurs
 *
 * @package    paygw_mtnafrica
 * @copyright  2023 Medical Access Uganda Limited
 * @author     Renaat Debleu <info@eWallah.net>
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class request_log extends \core\event\base {
    /**
     * Overrides the baseclass.
     *
     * @return void
     */
    protected function init() {
        $this->data['crud'] = 'c';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Returns localised general event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('request_log', 'paygw_mtnafrica');
    }

    /**
     * Returns non-localised event description with id's for admin use only.
     *
     * @return string
     */
    public function get_description() {
        $descstring = '';
        foreach ($this->other as $key => $value) {
            if (is_array($value)) {
                $value = json_encode($value);
            }
            $descstring .= "{$key}  : {$value} <br />";
        }
        return $descstring;
    }
}
